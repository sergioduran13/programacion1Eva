package _02ejemplos;

public class _07BubleFor {

	public static void main(String[] args) {
		//Mostrar por pantalla num 1 al 10
		
		//While
		
		int num = 1; 
		while(num<=10){
			System.out.println(num);
			num++;
		}

		//FOR
		
		for(int valor = 1; valor <= 10; valor ++){
			System.out.println(valor);
		}
	}

}
